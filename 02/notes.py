# Notes on Python programming

# 1. Python is a high-level, interpreted programming language known for its readability and simplicity.
# 2. It supports multiple programming paradigms, including procedural, object-oriented, and functional
#    programming.
# 3. Python has a large standard library that provides modules and functions for various tasks,
#    such as file I/O, regular expressions, and web development.
# 4. Python uses indentation to define code blocks, which enhances code readability.
# 5. Common data types in Python include integers, floats, strings, lists, tuples, sets, and dictionaries.
# 6. Python has dynamic typing, meaning that variable types are determined at runtime.
# 7. Functions in Python are defined using the 'def' keyword, and they can return values using the 'return' statement.
# 8. Python supports exception handling using 'try', 'except', 'finally', and 'raise' statements.
# 9. Python has a rich ecosystem of third-party libraries and frameworks, such as NumPy, Pandas,
#    Django, and Flask, which extend its capabilities for various applications.
# 10. Python is widely used in various fields, including web development, data science, machine learning,
#     automation, and scientific computing.

# Example of a simple Python function

def greet(name):
    """Function to greet a person by name."""
    return f"Hello, {name}!"
# Example usage
if __name__ == "__main__":
    print(greet("World"))   # Output: Hello, World! 

